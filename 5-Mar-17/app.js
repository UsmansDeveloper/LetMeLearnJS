var a = +prompt ("Enter Any percentage.");
if ( a >= 80 ){
    alert("Your Grade is A+")
}
else if ( a >= 70) {
    alert ("Your Grade is A")
}

else if ( a >= 60) {
    alert ("Your Grade is B")
}

else if ( a >= 50) {
    alert ("Your Grade is C")
}
else if ( a >= 40) {
    alert ("Your Grade is D")
}
else {
    alert ("You Are Failed!!")
}